# Citim numărul de la utilizator
numar = int(input("Introduceți un număr de la care să înceapă numărătoarea inversă: "))

# Bucla while pentru numărătoarea inversă
while numar >= 0:
    print(numar)  # Afișăm numărul curent
    numar -= 1  # Decrementăm numărul cu 1

print("Numărătoarea inversă s-a încheiat!")
