# Solicităm utilizatorului să introducă primul număr
numar1 = float(input("Introduceți primul număr: "))

# Solicităm utilizatorului să introducă al doilea număr
numar2 = float(input("Introduceți al doilea număr: "))

# Calculăm suma celor două numere
suma = numar1 + numar2

# Afișăm rezultatul
print(f"Suma celor două numere este: {suma}")
